# Изменение глобальных переменных или констант внутри функции

# Напишем функцию, которая будет считать, сколько раз её вызывали
ask_number = 0

def count_ask():
    global ask_number
    ask_number += 1
    print(f'Вы вызвали функцию {ask_number}-й раз.')

count_ask()  # => Вы вызвали функцию 1-й раз.
count_ask()  # => Вы вызвали функцию 2-й раз.
count_ask()  # => Вы вызвали функцию 3-й раз.

