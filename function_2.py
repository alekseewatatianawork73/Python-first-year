# Использование констант
# Напишем функцию для вывода цветов радуги на экран на английском или на русском языке

# константы для цветов радуги
ENGLISH_COLORS = ['red', 'orange', 'yellow', 'green',
                  'blue', 'indigo', 'violet']
RUSSIAN_COLORS = ['красный', 'оранжевый', 'желтый', 'зеленый',
                  'голубой', 'синий', 'фиолетовый']

# функция для вывода цветов радуги
def rainbow(num, language):
    if language == 'russian':
        print(RUSSIAN_COLORS[num])
    elif language == 'english':
        print(ENGLISH_COLORS[num])
    else:
        print('Неверный язык!')

rainbow(1, 'english')  # => orange
rainbow(2, 'russian')  # => желтый

